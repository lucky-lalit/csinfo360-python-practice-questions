# Exercise 7: Merge Sort
# Description: Implement merge sort algorithm
# Terminology:
#   - Divide and Conquer: Recursively divides and merges
#   - Stable Sort: Preserves original order of equal elements
#   - Time Complexity: O(n log n) in all cases
# Algorithm Steps:
#   1. Divide array into two halves
#   2. Recursively sort each half
#   3. Merge two sorted halves
# Example:
#   Input: [38, 27, 43, 3, 9, 82, 10]
#   Process:
#     Divide: [38,27,43,3] and [9,82,10]
#     Sort left: [3,27,38,43]
#     Sort right: [9,10,82]
#     Merge: [3,9,10,27,38,43,82]
#   Output: [3, 9, 10, 27, 38, 43, 82]
