# Exercise 4: Linear Search vs Binary Search Comparison
# Description: Compare performance of both search algorithms
# Terminology:
#   - Time Complexity Analysis: Measuring algorithm efficiency
#   - Benchmarking: Comparing actual performance
# Example:
#   Input: Sorted array with 1000 elements
#   Output:
#     Linear Search: ~500 comparisons on average
#     Binary Search: ~10 comparisons maximum
# Key Differences:
#   1. Linear Search works on any data, Binary requires sorted
#   2. Linear O(n) vs Binary O(log n) time complexity
#   3. Linear simpler implementation
# When to Use:
#   - Linear: Small datasets or unsorted data
#   - Binary: Large sorted datasets
