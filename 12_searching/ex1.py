# Exercise 1: Linear Search
# Description: Implement linear search algorithm to find an element in a list
# Terminology:
#   - Linear Search: Sequential search through each element
#   - Time Complexity: O(n) in worst case
#   - Space Complexity: O(1) - constant space
# Example:
#   Input: [12, 34, 54, 2, 3], target=34
#   Output: Found at index 1
# Algorithm Steps:
#   1. Start from the leftmost element
#   2. Compare each element with target
#   3. If match found, return index
#   4. If no match after all elements, return -1
# Advantages:
#   - Works on unsorted arrays
#   - Simple implementation
# Disadvantages:
#   - Inefficient for large datasets
